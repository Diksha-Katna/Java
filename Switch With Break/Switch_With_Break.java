package LearningJava;

public class Switch_With_Break {

	public static void main(String[] args) {

	

		
				

				   int c=1;
				   switch(c)
				
				{
				
							case 1:
								System.out.println("one ");
							    break;
								
								
							case 2:
								
								System.out.println("two ");
								break;
								
								
								 
							case 3:
								System.out.println("Three");
								break;
								
							
												
								
								default:
									System.out.println("This is default ");
				}
			}

		


	}


