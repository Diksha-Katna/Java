package LearningJava;

public class Switch_Statement {

	public static void main(String[] args) {
		
		    int c=1;
		   switch(c)
		
		{
		
					case 1:
						System.out.println("one ");
					
						
						
					case 2:
						
						System.out.println("two ");
						
						 
					case 3:
						System.out.println("Three");
					
										
						
						default:
							System.out.println("This is default ");
		}				
	}

}
