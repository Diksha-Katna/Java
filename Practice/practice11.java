public class practice11 {

	public static void main(String[] args) {

		
		char ch='d';
		switch(ch) {
		case 'a': System.out.println("This is first case a");
		
		case 'b': System.out.println("This is second case b");
		
		case 'd': System.out.println("This is third case d");
		break;
		
		
		default:
			
			System.out.println("This is default case ");
		
		}
	}

}
